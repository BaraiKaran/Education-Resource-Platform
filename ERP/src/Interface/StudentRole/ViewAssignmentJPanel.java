    public void populateCourses() {
        cmbCousreName.removeAllItems();
        for (Courses c : program.getCourses().getCourseList()) {
            for (UserAccount user : c.getStudents()) {
                if (ua.getId() == user.getId()) {
                    cmbCousreName.addItem(c);

                }
            }
        }
    }

